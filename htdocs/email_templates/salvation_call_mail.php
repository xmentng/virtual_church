Have you ever seen a butterfly? Or watched an early morning sunrise? Have you ever seen a flowing river? Or caught a peek at a rainbow? The Bible says in Romans 1 vs. 19: 20 "...because that which may be known of God is manifest in them, for God has shewed it to them. For the invisible things of Him from the creation of the world are clearly seen, being understood by the things that are made, even His eternal power and Godhead; so that they are without excuse: ".
<br />
