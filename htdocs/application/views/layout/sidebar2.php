                    <strong><p>Why We Pray</p></strong>

                    Praying is such an important part of our lives as Christians. First of all, we must  recognize prayer as a privilege as well as an honour. Every moment of prayer is an  investment in eternity and true prayer is a romance of righteousness. Our fellow-  ship is with the Father and with His Son Jesus Christ. Through prayer we partic-  ipate in the communion of the God-kind; it’s a throne-room caucus rendezvous: 
                    Jesus made it so! Oh, that the eyes of our understanding may be enlightened to see  this awesome truth and that we may walk in the light of it! What a time of rich  communion with the Lord prayer provides us!

                    <br/>Scripture enjoins us to pray without ceasing (1 Thessalonians 5:17). Prayer helps  to season our spirits as a dependable receptacle for God’s Spirit and power. Then  we can connect to His ideas, visions and leadings more easily in order to change  the world.

                    <br/>When we pray, we make tremendous power available, dynamic in its working,  causing changes in our favour. Certain prayer sessions are specially designed by  the Lord to help straighten out things in the spirit-realm regarding our immediate  or later future as individuals, families or ministries. How wonderful that we can  change circumstances and alter destinies in His Name through prayer! We serve a  kind, loving, great and gracious God who daily loads us with benefits (Psalms  68:19). During times of prayer, we can meditate on His goodness and appropriate  these benefits to our life’s circumstances.

                    <br/>It is God’s earnest desire to have a rich, vibrant and enduring fellowship with His  children;but how can they enjoy such communion with Him if they don’t know  how to pray effectively? This is one of the many reasons He impressed it upon me  to write “HOW TO PRAY EFFECTIVELY” and share with you some vital truths and  principles of effective prayer. This first volume will help establish for you a clear understanding of the rules of different prayers for different situations and how to apply them for your desired outcome.

