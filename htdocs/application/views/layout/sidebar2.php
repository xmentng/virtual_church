                    <p><strong>Introduction:</strong></p> Prayer is primarily our communication with the Lord. Communication has pur-  pose, and the purpose usually determines the mode and relevant details for the  communication to be effective. 

                    <br/>The disciples of Jesus were Jews and they knew how to pray religiously. After  walking with Jesus for a while, they realized that Jesus didn’t pray as they did. He  actually had results continually in a way that startled them. They finally said to the  Master, “Teach us to pray.” 

