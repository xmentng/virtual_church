<h3 style="color:#037DC8;">Class Modules</h3>
<div class="panel-group" id="accordion">
<div class="panel panel-default">
  <div class="panel-heading">
    <h4 class="panel-title"> <a data-toggle="collapse" data-parent="#accordion" href="#collapseOne" class=""> MODULE 1</a> </h4>
  </div>
  <div id="collapseOne" class="panel-collapse collapse in" style="height: auto;">
    <div class="panel-body">
      <p>For a long time, many of God&rsquo;s children equated meditation with just thinking or pondering over the Word. However meditation is much more than thinking deeply on a subject. That is just one aspect of meditation; there&rsquo;s more to it revealed in God&rsquo;s Word.</p>
      <p>Meditation is one of the Godly exercises that you must practice for a life of dominion and glory. It is a vital activity for every child of God, and its power cannot be overemphasized.</p>
      <p>In Joshua 1:8, the Bible says <em>&ldquo;this book of the law shall not depart out of thy mouth; but thou shalt meditate on it therein day and night, that thou mayest observe to do according to all that is written therein: for then thou shalt make thy way prosperous, and then thou shalt have good success.&rdquo;</em></p>
      <p><strong>Three levels of meditation</strong></p>
      <p>The word &ldquo;meditation&rdquo; in the verse of scripture above is translated from the Hebrew word &ldquo;hagar&rdquo; which means to &ldquo;imagine; mutter&rdquo;. It also means to &ldquo;roar&rdquo;. This immediately tells us that there are three levels of meditation, each of which is vital.</p>
      <ol>
        <li>The first level of meditation is to visualize God&rsquo;s Word as you study it. This means to ponder or think quietly on God&rsquo;s Word in pictures. As you keep these pictures in your mind, the Word will be driven into your spirit and will take root there.</li>
      </ol>
      <ol start="2">
        <li>The second level of meditation is to mutter God&rsquo;s Word. This means to speak God&rsquo;s Word under your breath, to yourself, over and over again. As you do this, the Word will become personal to you. It will go beyond the written page, beyond the reading of your mind and intellect, into your spirit. It will become God&rsquo;s Word to you, and not some distant words written in the Bible.</li>
      </ol>
      <ol start="3">
        <li>The third level of meditation is to roar God&rsquo;s Word! At this stage, you shout God&rsquo;s Word out loud. In the book of Isaiah 31:4, the Bible says, &ldquo;For thus hath the Lord spoken unto me, like as the lion, and the young lion roaring on his prey, when a multitude of shepherds is called forth against him, he will not be afraid of their voice, nor abase himself for the noise of them…&rdquo;</li>
      </ol>
      <p>In this verse of scripture, the Bible beautifully translated the same Hebrew word &ldquo;hagar&rdquo; as &ldquo;roar&rdquo;. The purpose of this kind of meditation is to drown every other thought in your consciousness, including thoughts that the devil may try to bring and cause the Word of God in your spirit to rise. When you begin shouting God&rsquo;s Word to yourself like this, some other folks may think something is wrong with you, but continue. The Bible says &ldquo;…for then thou shalt make thy way prosperous, and then thou shalt have good success&rdquo;.</p>
    </div>
  </div>
</div>
